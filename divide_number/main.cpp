vector<int> factors;

void get_factors(int n) {
    factors.clear();
    int i;
    for (i=1; i*i<n; i++) {
        if (n%i==0){
            factors.push_back(i);
            factors.push_back(n/i);
        }
    }
    if (i*i==n) factors.push_back(i);
}

ll solve(ll n,int fidx,int remain) {
    if (remain==0 && n==0) return 1;
    if (remain<0 || n<0 || fidx<0) return 0;
    return max(factors[fidx]*solve(n-factors[fidx],fidx,remain-1),solve(n,fidx-1,remain));
}

