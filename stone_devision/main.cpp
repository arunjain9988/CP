ll maxmoves(ll n,vector<ll> &moves,map<ll,ll>& dp) {
    if (n<=0) return 0;
    if (dp.find(n) != dp.end()) return dp[n];
    ll mx= 0;
    for (int i=0; i<moves.size(); i++) {
        if (n%moves[i] == 0 && n/moves[i] >1) {
            ll ans = 1+ (n/moves[i])*maxmoves(moves[i],moves,dp);
            mx = max(ans,mx);
        }
    }    
    return dp[n] = mx;
}

