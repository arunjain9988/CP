void quicksort(int A[],int n) {
    if (n<=1) return;
    int random_pivot = rand()%n;
    swap(A[0],A[random_pivot]);
    int i=1,j=n-1;
    while(i<j) {
        if (A[i] <= A[0]){
            i++;
        }
        else {
            swap(A[i],A[j]);
            j--;
        }
    }
    if (A[i] <= A[0]) swap(A[i],A[0]);
    else{
        i--;
        swap(A[i],A[0]);
    }
    // int mid = n/2;
    quicksort(A,i);
    quicksort(A+i+1,n-i-1);
}


