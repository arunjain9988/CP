bool valid(ll s,ll n) {
    ll sum  = 0;
    ll cur = 1;
    while(n>=cur) {
        sum += (n/cur);
        cur *=5;
    }
    return sum>=s;
}


ll solve(ll n) {
    ll low = 1,high = (ll)(1e16)/5;
    while(low<high) {
        ll mid = (low+high)/2;
        if (valid(n,mid)) {
            high = mid;
        }
        else {
            low = mid+1;
        }
    }
    return (high*5);
}


